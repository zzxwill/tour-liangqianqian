package mybean.data;

public class BookLine {
	String tour_line_name="";
	String user_name="";
	String book_time="";
	/**
	 * @return the tour_line_name
	 */
	public String getTour_line_name() {
		return tour_line_name;
	}
	/**
	 * @param tour_line_name the tour_line_name to set
	 */
	public void setTour_line_name(String tour_line_name) {
		this.tour_line_name = tour_line_name;
	}
	/**
	 * @return the user_name
	 */
	public String getUser_name() {
		return user_name;
	}
	/**
	 * @param user_name the user_name to set
	 */
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	/**
	 * @return the book_time
	 */
	public String getBook_time() {
		return book_time;
	}
	/**
	 * @param book_time the book_time to set
	 */
	public void setBook_time(String book_time) {
		this.book_time = book_time;
	}
	

}
