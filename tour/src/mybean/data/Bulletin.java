package mybean.data;

public class Bulletin {
	String bulletin_title="";
	String bulletin_context="";
	String bulletin_time="";
	/**
	 * @return the bulletin_title
	 */
	public String getBulletin_title() {
		return bulletin_title;
	}
	/**
	 * @param bulletin_title the bulletin_title to set
	 */
	public void setBulletin_title(String bulletin_title) {
		this.bulletin_title = bulletin_title;
	}
	/**
	 * @return the bulletin_context
	 */
	public String getBulletin_context() {
		return bulletin_context;
	}
	/**
	 * @param bulletin_context the bulletin_context to set
	 */
	public void setBulletin_context(String bulletin_context) {
		this.bulletin_context = bulletin_context;
	}
	/**
	 * @return the bulletin_time
	 */
	public String getBulletin_time() {
		return bulletin_time;
	}
	/**
	 * @param bulletin_time the bulletin_time to set
	 */
	public void setBulletin_time(String bulletin_time) {
		this.bulletin_time = bulletin_time;
	}
	
}
